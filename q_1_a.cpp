// What is stack memory

// Stack memory is a section of computer memory used to store temporary data and local variables during program execution.It operates in a Last In  First  Out fashion, where the most recently added data is the first to be removed.

// What is Heap Memory

// Heap memory, also known as dynamic memory, is a region of computer memory used for dynamically allocated data. It allows for flexible memory allocation and deallocation during runtime. Unlike stack memory, it does not follow a specific order for memory access or deallocation.